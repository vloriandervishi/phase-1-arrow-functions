/**
 * Divides a number by 100.
 * @param {number} num - The number to divide.
 * @returns The result of dividing the number by 100.
 */
const divide = function (num) {
  return num / 100;
};

/**
 * Calculates the square of a given number.
 * @param {number} num - The number to calculate the square of.
 * @returns The square of the given number.
 */
const square = (num) => {
  return num * num;
};
/**
 * Adds two numbers together.
 * @param {number} prev - The first number to add.
 * @param {number} curr - The second number to add.
 * @returns The sum of the two numbers.
 */
const add = (prev, curr) => {
  return prev + curr;
};
